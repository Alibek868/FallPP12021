#include <iostream>

using namespace std;

int main(){

    int n;
    cin >> n;

    cout << "The next number for the number ";
    cout << n;
    cout << " is ";
    cout << n + 1;
    cout << "." << endl;


    cout << "The previous number for the number ";
    cout << n;
    cout << " is ";
    cout << n - 1;
    cout << "." << endl;

    return 0;
}